	<script>
    // Variable to store orders
    let order_array = [];

    window.onload = function(){
      // Add event to all forms having class name form_add_to_cart
      let add_to_cart_forms = document.querySelectorAll('form.form_add_to_cart');
      for (let i = 0; i < add_to_cart_forms.length; i++) {
        add_to_cart_forms[i].addEventListener('submit', function(e) {
          // Prevent page from reloading after form submission
          e.preventDefault();

          // Determine title, price and quantity
          let id = e.target.id;
          let title = document.querySelector('#' + id + ' span.menu-name').innerText;
          let price = document.querySelector('#' + id + ' span.menu-price').innerText;
          let quantity = e.target.elements.namedItem('qty').value;



          // Add to order_array
          addMenu(title, parseInt(price), parseInt(quantity));
          alert("Menu added to cart");
        });
      }

      // Submit Order when order button is pressed;
      document.getElementById('btn-order').addEventListener('click', submit_order);
    }

    /*
     * Function to add ordered menu to array
     */
     function addMenu(title, price, quantity) {
       // set order object
       let obj = {
         title: title,
         price: price,
         quantity: quantity
       };

       // Add object to order array
       order_array.push(obj);
     }


     /*
      * Function to submit order
      */
      function submit_order(){
        let submit_order_form = document.getElementById('submit-order');
        let orders = document.getElementById('orders');
        orders.value = JSON.stringify(order_array);
        submit_order_form.submit();
      }
  </script>
